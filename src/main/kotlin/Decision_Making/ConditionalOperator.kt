package Decision_Making

fun main(args: Array<String>) {
    var firstNumber = 5
    var secondNumber = 7

    println("$firstNumber > $secondNumber --> ${firstNumber > secondNumber}")
    println("$firstNumber < $secondNumber --> ${firstNumber < secondNumber}")
    println("$firstNumber <= $secondNumber --> ${firstNumber <= secondNumber}")
    println("$firstNumber >= $secondNumber --> ${firstNumber >= secondNumber}")
    println("$firstNumber == $secondNumber --> ${firstNumber == secondNumber}")
    println("$firstNumber != $secondNumber --> ${firstNumber != secondNumber}")
}