package Variable

fun main(args: Array<String>) {
    //String
    var myString:String = "this is a a String"
    println(myString)

    //Integer
    var myInt:Int = 2023
    println(myInt)

    //Double
    var myDouble:Double = 3.14
    println(myDouble)

    var myBoolean:Boolean = true
    println(myBoolean)

    var myFloat:Float = 2.5F
    println(myFloat)

    var myChar:Char = 'a'
    println(myChar)
}