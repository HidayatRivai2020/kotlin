package variable

fun main(args: Array<String>) {
    var name = "Zack"
    println(name)

    name = "Stu"
    println(name)

    var catName:String? = null
    catName = "meow"
    println(catName)

    name = "This var use an escape character for double quotes: \"Stu\""
    println(name)

    name = "This var use an escape character for the backslash: \\ and /"
    println(name)

}